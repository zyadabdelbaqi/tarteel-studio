const domCache = new Map();

const isAlive = (el) => el && el.isConnected;

const getEl = (id, isRequired = false) => {
    let el = domCache.get(id);
    if (!isAlive(el)) {
        domCache.delete(id); // تنظيف العنصر الميت من الكاش لمنع تسريب الذاكرة
        el = document.getElementById(id);
        if (el) domCache.set(id, el);
    }
    if (isRequired && !el) throw new Error(`Missing required UI element: #${id}`);
    return el;
};

const getSel = (selector, isRequired = false) => {
    let el = domCache.get(selector);
    if (!isAlive(el)) {
        domCache.delete(selector); // تنظيف العنصر الميت من الكاش لمنع تسريب الذاكرة
        el = document.querySelector(selector);
        if (el) domCache.set(selector, el);
    }
    if (isRequired && !el) throw new Error(`Missing required UI element: ${selector}`);
    return el;
};

